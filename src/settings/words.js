const getWords = function (n) {
  let gameWords = [];

  for (let i = 0; i < n; i++) {
    let randomWord = Math.floor(Math.random() * (words.length - 1) + 1);

    if (gameWords.indexOf(words[randomWord]) >= 0) {
      i--
    } else {
      gameWords.push(words[randomWord]);
    }
  }

  return gameWords;
}

const getRole = function (n) {
  let setRole = [];
  let typeCard = ['red', 'blue', 'killer', 'civil'];
  let starter = typeCard[Math.floor(Math.random() * 2)];

  for (let i = 0; i < n; i++) {
    setRole.push(typeCard[3]);
  }

  for (let i = 0; i < 9; i++) {
     let firstTeam = Math.floor(Math.random() * n)

     setRole[firstTeam]  === starter ? i-- : setRole[firstTeam] = starter;

   }

 for (let i = 0; i < 8; i++) {
   let pValue;
   let pointer = Math.floor(Math.random() * n)
   let oldValue = setRole[pointer];

   starter === typeCard[0] ? pValue = typeCard[1] : pValue = typeCard[0];

   if (oldValue === typeCard[3]) {
     setRole[pointer] = pValue
   } else {
     i--
   }
 }

  for (let i = 0; i < 1; i++) {
    
    let pValue = typeCard[2];
    let pointer = Math.floor(Math.random() * n);
    let oldValue = setRole[pointer];
     
    if (oldValue === typeCard[3]) {
       setRole[pointer] = pValue
     } else {
       i--
     }
   
    }

  return setRole;
}

const setGame = function (n) {
  let gameSet = [];
  let words = getWords(n);
  let roles = getRole(n);
  for (let i = 0; i < words.length; i++) {
    let card = {
      'word' : `${words[i]}`,
      'role' : `${roles[i]}` 
    }
    gameSet.push(card);
  }
  return gameSet;
}

export default setGame;
